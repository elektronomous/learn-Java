public class MathMethod {
    public static void main(String[] args) {
        System.out.println(Math.pow(2.0, 3.0));
        // Raise a number to a power
        System.out.println(Math.sqrt(4.0));
        // Computes the square root of a number
        System.out.println(Math.abs(-2.0));
        // Computes the absolute value of a number
        System.out.println(Math.max(3.0, 5.5));
        // Finds the larger of two numbers
        System.out.println(Math.min(10.0, -2.0));
        // Finds the smaller of two numbers
        System.out.println(Math.round(5.5));    // 6
        System.out.println(Math.round(5.4));    // 5
        // Round to the next larger integer

    }
}
