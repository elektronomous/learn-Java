/*
        '       '                   [ ]
       (\______/)                  (   )
        (__oo__)                    |>|
           (O)                   __/===\__
         __||__     \)          //| o=o |\\
     [] /______\ [] /         <]  | o=o |  [>
     /  \______/  \/              \=====/
    /     /__\                   / / | \ \
   (\    /____\                 <_________> 

*/

public class ASCIIArt {
    public static void main(String[] args) {
        System.out.println("        '       '                   [ ]");              // 1
        System.out.println("       (\\______/)                  (   )");            // 2
        System.out.println("        (__oo__)                    |>|");              // 3
        System.out.println("           (O)                   __/===\\__");          // 4
        System.out.println("         __||__     \\)          //| o=o |\\\\");       // 5
        System.out.println("     [] /______\\ [] /         <]  | o=o |  [>");       // 6
        System.out.println("     /  \\______/  \\/              \\=====/");         // 7
        System.out.println("    /     /__\\                   / / | \\ \\");        // 8
        System.out.println("   (\\    /____\\                 <_________>");        // 9
    }
}